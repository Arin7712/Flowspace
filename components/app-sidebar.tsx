type Workspace = {
  name: string
  id: string
}

type DBUser = {
  preferredName: string | null,
  email: string,
  id: string
}

export function AppSidebar({workspace,initialUser} : {workspace: Workspace, initialUser: DBUser}) {


  const [user, setUser] = React.useState(initialUser);

React.useEffect(() => {
  const socket = io(process.env.NEXT_PUBLIC_SOCKET_URL!, {
    auth: { userId: user.id },
  });

  // Example: listen for events
  socket.on("preferred_name_updated", (data) => {
    setUser((prev:any) => ({ ...prev, preferredName: data.preferredName }));
  });

  // Cleanup function
  return () => {
    socket.disconnect(); // âš¡ only cleanup here
  };
}, [user.id]); // or [] if userId never changes

  return (
    <Sidebar collapsible="icon">
      <SidebarHeader>
        <NavUser workspace={workspace} user={user}/>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavProjects projects={data.projects} />
      </SidebarContent>
      <SidebarFooter>
        <TeamSwitcher teams={data.teams} />
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  );
}
